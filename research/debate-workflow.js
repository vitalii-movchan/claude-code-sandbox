export const meta = {
  name: 'advocate-vs-skeptic-debate',
  description: 'Дебаты адвокат vs скептик про любой ресурс/подписку/курс: автосбор фактов, прямой обмен репликами по раундам, финальный вердикт арбитра',
  phases: [
    { title: 'Сбор фактов' },
    { title: 'Раунды дебатов' },
    { title: 'Синтез вывода' },
  ],
}

// ── Параметры запуска ──────────────────────────────────────────────
// Передаются через args при вызове Workflow. Примеры:
//   args: { topic: "курс по криптовалюте в Telegram-канале @cryptoschool", url: "https://...", rounds: 3 }
//   args: { topic: "подписка на сервис X", facts: "...уже собранная фактура...", rounds: 2 }
//   args: "neserioznoeit.com/it-garage"   // можно просто строкой — это станет topic
//
// Поля args (все опциональны, кроме topic):
//   topic   — что оцениваем (название ресурса/курса/подписки + контекст). ОБЯЗАТЕЛЬНО.
//   url     — ссылка на ресурс для автосбора фактов (если есть).
//   facts   — готовая фактура. Если задана — этап автосбора пропускается.
//   rounds  — число раундов дебатов (по умолчанию 3).
//   decision — формулировка решения, напр. "оформлять ли подписку" (по умолчанию "вступать ли / оформлять ли").
// ───────────────────────────────────────────────────────────────────

const cfg = typeof args === 'string' ? { topic: args } : (args || {})
const TOPIC = cfg.topic || 'оцениваемый ресурс (тема не задана — уточни у пользователя)'
const URL = cfg.url || ''
const ROUNDS = cfg.rounds || 3
const DECISION = cfg.decision || 'стоит ли вступать / оформлять подписку'

phase('Сбор фактов')

// Фактура: либо передана пользователем, либо собирается агентом-исследователем через веб-поиск.
let FACTS = cfg.facts
if (!FACTS) {
  const FACTS_SCHEMA = {
    type: 'object',
    additionalProperties: false,
    required: ['facts'],
    properties: {
      facts: { type: 'string', description: 'Сжатая, но полная фактура о ресурсе: что это, что предлагают, стоимость, формат, для кого, обещания, автор/организатор, наличие независимых отзывов, доступность бесплатных альтернатив. Только проверяемые факты, без оценок.' },
    },
  }
  const research = await agent(
    `Ты — АГЕНТ-ИССЛЕДОВАТЕЛЬ. Собери объективную фактуру о ресурсе для последующих дебатов «${DECISION}».
ОБЪЕКТ ОЦЕНКИ: ${TOPIC}${URL ? `\nURL: ${URL}` : ''}
Используй веб-поиск (WebSearch) и при необходимости загрузку страниц (WebFetch). Найди и зафиксируй ТОЛЬКО проверяемые факты:
- Что это за ресурс, что именно предлагают (контент/доступ/услуга).
- Стоимость, модель оплаты, длительность, формат.
- Для кого, какие обещания/заявленные результаты.
- Кто автор/организатор, его бэкграунд.
- Есть ли независимые сторонние отзывы / проверяемая статистика результатов.
- Доступны ли бесплатные альтернативы той же информации/услуги.
- Любые маркетинговые механики (срочность, рост цены, дефицит).
НЕ давай оценок «хорошо/плохо» — только факты. Если чего-то найти не удалось, прямо отметь «данных нет».`,
    { label: 'исследователь', phase: 'Сбор фактов', schema: FACTS_SCHEMA }
  )
  FACTS = `ФАКТЫ ОБ ОБЪЕКТЕ «${TOPIC}»:\n${research.facts}`
  log(`Фактура собрана автоматически (${FACTS.length} симв.)`)
} else {
  FACTS = `ФАКТЫ ОБ ОБЪЕКТЕ «${TOPIC}»:\n${FACTS}`
  log('Использую переданную фактуру (автосбор пропущен)')
}

// ── Дебаты ─────────────────────────────────────────────────────────
const REPLY_SCHEMA = {
  type: 'object',
  additionalProperties: false,
  required: ['reply', 'concessions'],
  properties: {
    reply: { type: 'string', description: 'Реплика в дебатах: возражения и/или новые аргументы, по делу, на русском' },
    concessions: { type: 'string', description: 'Что из позиции оппонента ты честно признаёшь верным (или "ничего нового")' },
  },
}

let advocateLast = '(оппонент ещё не высказывался — открывай дебаты своими аргументами ЗА)'
let skepticLast = '(оппонент ещё не высказывался — открывай дебаты своими аргументами ПРОТИВ)'
const transcript = []

phase('Раунды дебатов')

for (let r = 1; r <= ROUNDS; r++) {
  // Адвокат отвечает на последнюю реплику скептика
  const adv = await agent(
    `Ты — АГЕНТ-АДВОКАТ в дебатах о том, ${DECISION} (объект: ${TOPIC}). Раунд ${r} из ${ROUNDS}. Твоя позиция: ЗА.
${FACTS}
Последняя реплика оппонента-СКЕПТИКА:
"""${skepticLast}"""
Ответь на неё: где обоснованно — возрази, где скептик прав — ЧЕСТНО признай (не защищай неоправданное). ${r === ROUNDS ? 'Это финальный раунд — формулируй уже взвешенную, уточнённую позицию: при каких условиях решение оправдано.' : 'Можешь добавить новые аргументы ЗА.'} Опирайся только на факты выше, не выдумывай. Цель — истина, а не победа.`,
    { label: `адвокат-р${r}`, phase: 'Раунды дебатов', schema: REPLY_SCHEMA }
  )
  advocateLast = adv.reply
  transcript.push({ round: r, side: 'Адвокат', reply: adv.reply, concessions: adv.concessions })

  // Скептик отвечает на свежую реплику адвоката
  const skp = await agent(
    `Ты — АГЕНТ-СКЕПТИК в дебатах о том, ${DECISION} (объект: ${TOPIC}). Раунд ${r} из ${ROUNDS}. Твоя позиция: ПРОТИВ / акцент на рисках.
${FACTS}
Последняя реплика оппонента-АДВОКАТА:
"""${advocateLast}"""
Ответь на неё: где обоснованно — контраргументируй, где адвокат прав — ЧЕСТНО признай (не возражай ради возражения). ${r === ROUNDS ? 'Это финальный раунд — формулируй уже взвешенную, уточнённую позицию: при каких условиях решение НЕ оправдано.' : 'Можешь добавить новые риски.'} Опирайся только на факты выше, не выдумывай. Цель — истина, а не победа.`,
    { label: `скептик-р${r}`, phase: 'Раунды дебатов', schema: REPLY_SCHEMA }
  )
  skepticLast = skp.reply
  transcript.push({ round: r, side: 'Скептик', reply: skp.reply, concessions: skp.concessions })
}

// ── Синтез ─────────────────────────────────────────────────────────
phase('Синтез вывода')

const VERDICT_SCHEMA = {
  type: 'object',
  additionalProperties: false,
  required: ['converged_on', 'join_if', 'avoid_if', 'verdict'],
  properties: {
    converged_on: { type: 'string', description: 'На чём обе стороны сошлись по итогам раундов' },
    join_if: { type: 'array', items: { type: 'string' }, description: 'Условия, при которых решение оправдано' },
    avoid_if: { type: 'array', items: { type: 'string' }, description: 'Условия, при которых решение НЕ оправдано' },
    verdict: { type: 'string', description: 'Финальный взвешенный вердикт в 2-3 предложениях' },
  },
}

const synthesis = await agent(
  `Ты — НЕЙТРАЛЬНЫЙ АРБИТР дебатов о том, ${DECISION} (объект: ${TOPIC}). Ниже полный транскрипт ${ROUNDS}-раундовых дебатов адвоката и скептика (с их взаимными уступками). Сведи к общему знаменателю честно, не подыгрывая ни одной стороне.
${FACTS}
ТРАНСКРИПТ ДЕБАТОВ (JSON):
${JSON.stringify(transcript, null, 2)}
Дай: на чём стороны сошлись; условия, при которых решение оправдано; условия, при которых не стоит; финальный взвешенный вердикт.`,
  { label: 'арбитр-синтез', phase: 'Синтез вывода', schema: VERDICT_SCHEMA }
)

return { topic: TOPIC, rounds: ROUNDS, transcript, synthesis }
